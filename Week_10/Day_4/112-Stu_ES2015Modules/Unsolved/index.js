function makeASCIIArray(string) {
  var arr = string.split("");
  var array = [];
  var i = 0;
  array.push([]);
  for (let key in arr) {
    var char = arr[key];
    if (char == "\n") {
      array.push([]);
      i++;
    }
    array[i].push(char);
  }
  return array;
}


